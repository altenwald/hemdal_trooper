defmodule HemdalTrooper do
  @moduledoc """
  Hemdal is a system for Alert/Alarm very customizable.

  Trooper is a SSH agent which let us to access servers easily to run
  commands and copy files.

  The Hemdal Trooper plugin let Hemdal use the capabilities of Trooper
  to access to the remote machines using SSH and perform the execution
  of the checks on remote retrieving the results.
  """
end
